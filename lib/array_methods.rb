def using_include(array, element)
  included = array.include?(element)
  return included
end

def using_sort(array)
  sorted = array.sort
  return sorted
end

def using_reverse(array)
  reversed = array.reverse
  return reversed
end

def using_first(array)
  first = array.first
  return first
end

def using_last(array)
  last = array.last
  return last
end

def using_size(array)
  size = array.size
  return size
end
